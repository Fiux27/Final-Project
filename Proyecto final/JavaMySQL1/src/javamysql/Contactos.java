    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        int fila = tabladatos.getSelectedRow();
        if (fila >= 0) {
            txtAlumno.setText(tabladatos.getValueAt(fila, 0).toString());
            txtNombre.setText(tabladatos.getValueAt(fila, 1).toString());
            txtApellido.setText(tabladatos.getValueAt(fila, 2).toString());
            txtTelefono.setText(tabladatos.getValueAt(fila, 3).toString());
            txtEmail.setText(tabladatos.getValueAt(fila, 4).toString());

        } else {
            JOptionPane.showMessageDialog(null, "Fila no seleccionada");
        }

    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        try {
            PreparedStatement pps = cn.prepareStatement("UPDATE contact_list SET student='" + txtAlumno.getText() + "', name='" + txtNombre.getText()
                    + "', lastname='" + txtApellido.getText() + "', callphone number='" + txtTelefono.getText() + "', e-mail='" + txtEmail.getText()
                    + "' WHERE alumno='" + txtAlumno.getText() + "'");
            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos Actualizados");
            limpiar();
            mostrartabla();

        } catch (SQLException ex) {
            Logger.getLogger(Administrador.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int fila = tabladatos.getSelectedRow();
        String valor = tabladatos.getValueAt(fila, 0).toString();
        if (fila >= 0) {
            try {
                PreparedStatement pps = cn.prepareStatement("DELETE FROM contact_list WHERE student='" + valor + "'");
                pps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Dato Eliminado");
                mostrartabla();
            } catch (SQLException ex) {
                Logger.getLogger(Administrador.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

