    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            PreparedStatement pps = cn.prepareStatement("INSERT INTO user(name, password, student) VALUES (?,?,?)");
            pps.setString(1, txtNombre.getText());
            pps.setString(2, txtCont.getText());
            pps.setString(3, txtAlumno.getText());
            pps.executeUpdate();
            mostrartabla();
            JOptionPane.showMessageDialog(null, "Datos Guardados");

        } catch (SQLException ex) {
            Logger.getLogger(Contactos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

